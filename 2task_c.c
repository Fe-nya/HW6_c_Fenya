#include <stdlib.h>

char* join(char* words[], int n) {
    // Вычисляем общую длину всех строк, которые хотим объединить
    int total_len = 0;
    for (int i = 0; i < n; i++) {
        int j = 0;
        while (words[i][j] != 0) {
            total_len++;
            j++;
        }
        total_len++; // добавляем место под пробел после каждой строки
    }

    // Выделяем память под новую строку, которая будет представлять собой объединение всех строк
    char* result = malloc(total_len); // выделяем память, достаточную для хранения объединенных строк
    result[0] = 0; // начинаем объединенную строку пустой строкой
    int k = 0; // указатель на текущую позицию в объединенной строке

    // Проходимся по массиву строк и объединяем их, добавляя между ними пробелы
    for (int i = 0; i < n; i++) {
        int j = 0;
        while (words[i][j] != 0) {
            result[k] = words[i][j];
            j++;
            k++;
        }
        if (i < n - 1) { // если не последняя строка, добавляем пробел
            result[k] = ' ';
            k++;
        }
    }

    result[k] = 0; // добавляем завершающий нулевой символ, чтобы обозначить конец строки
    return result; // возвращаем объединенную строку
}